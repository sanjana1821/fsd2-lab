// Arrow Functions

// 1. Addition
const add = (a: number, b: number): number => a + b;
console.log("Sum =", add(10, 20));

// 2. Square
const square = (num: number): number => num * num;
console.log("Square =", square(5));

// 3. Greeting
const greet = (name: string): string => "Hello, " + name + "!";
console.log(greet("Hema"));